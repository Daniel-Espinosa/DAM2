    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {

            Connection con = ConexionBBDD.getInstance().getConnection();

            Map parametros = new HashMap();
            int i = Integer.parseInt(jTextFieldID.getText());
            parametros.put("Id de cliente", i);

            JasperPrint print = JasperFillManager.fillReport("informes/Facturacion Streamer 2023.jasper", parametros, con);
            JasperExportManager.exportReportToPdfFile(print, "Facturacion Streamer 2023.pdf");

        } catch (JRException ex) {
            Logger.getLogger(DI_Examen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

